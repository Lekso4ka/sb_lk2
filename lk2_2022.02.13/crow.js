/*
    n ворон
    0 < n < 1000

    ворона  1 21 31 41 191 
    вороны  2-4 22-24 52 64 183
    ворон   0 5-10 11-20 25-30 211
*/

/*
    211 % 10 = 1
    211 % 100 = 11
*/

let n = Math.round(Math.random() * 1000);
let word;
if (n % 100 < 11 || n % 100 > 14) {
    if (n % 10 === 1) {
        word = "ворона";
    } else if ((n % 10 >= 2) && (n % 10 <= 4)) {
        word = "вороны";
    } else {
        word = "ворон";
    }
} else {
    word = "ворон";
}

console.log(`${n} ${word}`);